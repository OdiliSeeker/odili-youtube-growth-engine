_VERIFIED_UPGRADE_KEY = "resources_verified_v2"


def apply_verified_source_upgrade(db: Session) -> None:
    """One-time forced upgrade to the ministry-specified verified source set.

    Wipes all rows in the 5 fixed sections and reseeds them from _SEED (every
    link verified live). Runs exactly once — guarded by an AppSetting flag —
    so admin edits made AFTER the upgrade are never overwritten.
    """
    from app.models.db_models import AppSetting

    if db.get(AppSetting, _VERIFIED_UPGRADE_KEY) is not None:
        return
    db.query(PlaylistResource).filter(
        PlaylistResource.category.in_(tuple(SECTIONS.keys()))
    ).delete(synchronize_session=False)
    for i, item in enumerate(_SEED):
        seo_title, seo_desc, seo_kw = _seo_for(
            item["title"], item["category"], item["description"]
        )
        db.add(
            PlaylistResource(
                title=item["title"],
                category=item["category"],
                description=item["description"],
                relevance=item["relevance"],
                source_type=item["source_type"],
                source_name=item["source_name"],
                link=_safe_url(item.get("link")),
                tags=json.dumps(item.get("tags", [])),
                seo_title=seo_title,
                seo_description=seo_desc,
                seo_keywords=json.dumps(seo_kw),
                sort_order=i,
            )
        )
    db.add(AppSetting(key=_VERIFIED_UPGRADE_KEY, value="done"))
    db.commit()
    logger.info(
        "Applied verified-source upgrade: %d resources across %d sections.",
        len(_SEED), len(SECTIONS),
    )


