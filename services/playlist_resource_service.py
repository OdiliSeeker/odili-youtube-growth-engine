"""
Playlist Resources: an evergreen authority knowledge base.

Four fixed sections of trusted historical/doctrinal sources (papal history,
councils & heresies, trials of the Church, Church Fathers). Each resource
carries a summary + "why it matters" relevance blurb and an optional YouTube
teaching link — the public page shows insight FIRST, then the video CTA
(authority → conversion, never a bare link drop).

Deterministic-first: SEO meta (title/description/keywords) is generated by
pure-Python templates at save time — no AI, no quota, never 402.
"""

import json
import logging

from sqlalchemy.orm import Session

from app.models.db_models import PlaylistResource

logger = logging.getLogger(__name__)

SOURCE_TYPES = ("book", "council", "father", "encyclopedia", "document")
ALLOWED_TAGS = ("papacy", "heresy", "councils", "fathers", "persecution")

SECTIONS: dict[str, str] = {
    "papal-history": "Papal History",
    "heresies-councils": "Heresies & Councils",
    "trials-of-the-church": "Trials of the Church",
    "church-fathers": "Church Fathers",
    "playlist-resources": "Core Teachings",
}


def _safe_url(url: str | None) -> str | None:
    u = (url or "").strip()
    return u if u.lower().startswith(("http://", "https://")) else None


def _seo_for(title: str, category: str, description: str) -> tuple[str, str, list[str]]:
    """Deterministic SEO meta — pure templates, no AI."""
    section = SECTIONS.get(category, "Catholic History")
    seo_title = f"{title} — {section} Explained (Catholic Sources)"[:300]
    base_desc = (description or "").strip().replace("\n", " ")
    seo_description = (
        base_desc[:440] + " Explore this trusted Catholic source and what it reveals."
    )[:500]
    keywords = [
        title.lower(),
        section.lower(),
        "catholic history",
        "early church",
        "catholic sources",
    ]
    return seo_title, seo_description, keywords


def _serialize(r: PlaylistResource) -> dict:
    def _load(raw: str) -> list:
        try:
            v = json.loads(raw or "[]")
            return v if isinstance(v, list) else []
        except (ValueError, TypeError):
            return []

    return {
        "id": r.id,
        "title": r.title,
        "category": r.category,
        "category_label": SECTIONS.get(r.category, r.category),
        "description": r.description,
        "relevance": r.relevance,
        "source_type": r.source_type,
        "source_name": r.source_name,
        "link": _safe_url(r.link),
        "video_url": _safe_url(r.video_url),
        "tags": _load(r.tags),
        "seo_title": r.seo_title,
        "seo_description": r.seo_description,
        "seo_keywords": _load(r.seo_keywords),
        "sort_order": r.sort_order,
        "created_at": r.created_at.isoformat() if r.created_at else None,
    }


def list_resources(db: Session, category: str | None = None) -> list[dict]:
    q = db.query(PlaylistResource)
    if category:
        q = q.filter(PlaylistResource.category == category)
    rows = q.order_by(
        PlaylistResource.category, PlaylistResource.sort_order, PlaylistResource.id
    ).all()
    return [_serialize(r) for r in rows]


def grouped(db: Session) -> list[dict]:
    """Sections in fixed order, each with its resources (empty sections skipped)."""
    all_items = list_resources(db)
    out = []
    for slug, label in SECTIONS.items():
        items = [i for i in all_items if i["category"] == slug]
        if items:
            out.append({"slug": slug, "label": label, "resources": items})
    return out


def get_resource(db: Session, resource_id: int) -> dict | None:
    row = db.get(PlaylistResource, resource_id)
    return _serialize(row) if row else None


def create_resource(
    db: Session,
    *,
    title: str,
    category: str,
    description: str = "",
    relevance: str = "",
    source_type: str = "book",
    source_name: str = "",
    link: str | None = None,
    video_url: str | None = None,
    tags: list[str] | None = None,
    sort_order: int = 0,
) -> dict:
    if category not in SECTIONS:
        raise ValueError(f"Unknown category: {category}")
    if source_type not in SOURCE_TYPES:
        source_type = "book"
    clean_tags = [t for t in (tags or []) if t in ALLOWED_TAGS]
    seo_title, seo_desc, seo_kw = _seo_for(title, category, description)
    row = PlaylistResource(
        title=title.strip()[:300],
        category=category,
        description=(description or "").strip(),
        relevance=(relevance or "").strip(),
        source_type=source_type,
        source_name=(source_name or "").strip()[:300],
        link=_safe_url(link),
        video_url=_safe_url(video_url),
        tags=json.dumps(clean_tags),
        seo_title=seo_title,
        seo_description=seo_desc,
        seo_keywords=json.dumps(seo_kw),
        sort_order=sort_order,
    )
    db.add(row)
    db.commit()
    db.refresh(row)
    return _serialize(row)


def update_resource(db: Session, resource_id: int, fields: dict) -> dict | None:
    row = db.get(PlaylistResource, resource_id)
    if row is None:
        return None
    if "title" in fields and fields["title"]:
        row.title = str(fields["title"]).strip()[:300]
    if "category" in fields and fields["category"] in SECTIONS:
        row.category = fields["category"]
    for key in ("description", "relevance", "source_name"):
        if key in fields and fields[key] is not None:
            setattr(row, key, str(fields[key]).strip())
    if "source_type" in fields and fields["source_type"] in SOURCE_TYPES:
        row.source_type = fields["source_type"]
    if "link" in fields:
        row.link = _safe_url(fields["link"])
    if "video_url" in fields:
        row.video_url = _safe_url(fields["video_url"])
    if "tags" in fields and isinstance(fields["tags"], list):
        row.tags = json.dumps([t for t in fields["tags"] if t in ALLOWED_TAGS])
    if "sort_order" in fields and fields["sort_order"] is not None:
        try:
            row.sort_order = int(fields["sort_order"])
        except (TypeError, ValueError):
            pass
    # Refresh deterministic SEO meta after any content change.
    row.seo_title, row.seo_description, kw = _seo_for(row.title, row.category, row.description)
    row.seo_keywords = json.dumps(kw)
    db.commit()
    db.refresh(row)
    return _serialize(row)


def delete_resource(db: Session, resource_id: int) -> bool:
    row = db.get(PlaylistResource, resource_id)
    if row is None:
        return False
    db.delete(row)
    db.commit()
    return True


def promote_resource(db: Session, resource_id: int) -> dict | None:
    """Content-engine tie-in: Resource → Topic → Email draft (funnel loop).

    Creates an audience Topic and an EmailQueue draft (draft→approve→send —
    nothing bulk-sends). Additive and idempotent-ish via draft dedup.
    """
    item = get_resource(db, resource_id)
    if item is None:
        return None
    from app.services.topic_service import create_topic
    from app.services.email_queue_service import create_draft

    topic = create_topic(
        db,
        title=f"{item['title']} — what it reveals about the early Church"[:200],
        description=(item["description"] or "")[:500],
        source="playlist_resource",
    )
    body_lines = [
        f"Most people have never read {item['title']} — and it changes everything.",
        "",
        (item["description"] or "").strip(),
        "",
        (item["relevance"] or "").strip(),
        "",
        "I break this down in a short teaching — watch it before you form an opinion.",
    ]
    draft = create_draft(
        db,
        subject=f"What {item['title']} actually says"[:300],
        body="\n".join(l for l in body_lines if l is not None),
        source="playlist_resource",
        topic_id=topic.get("id"),
        video_url=item["video_url"],
    )
    return {
        "ok": True,
        "topic": topic,
        "email_draft_id": draft.id if draft else None,
    }


